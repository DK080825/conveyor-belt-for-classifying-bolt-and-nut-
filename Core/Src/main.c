/* USER CODE BEGIN 4 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM4)
    {
        // Cập nhật tốc độ encoder
        Encoder_Update(&motor_enc);
        apply_average_filter(&filter_inst, motor_enc.rpm, &motor_vel);

        // ======================
        // START: Conveyor running
        // ======================
        if (conveyor_start == 1)
        {
            // PID chạy theo target speed
            error = (float)g_target_speed - motor_vel;

            if (pid_inst.p_gain != 0 || pid_inst.i_gain != 0 || pid_inst.d_gain != 0)
            {
                apply_pid(&pid_inst, error);

                if (pid_inst.output > 0)
                {
                    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, pid_inst.output);
                    HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, GPIO_PIN_SET);
                    HAL_GPIO_WritePin(IN1_GPIO_Port, IN2_Pin, GPIO_PIN_RESET);
                }
                else
                {
                    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, -pid_inst.output);
                    HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, GPIO_PIN_RESET);
                    HAL_GPIO_WritePin(IN1_GPIO_Port, IN2_Pin, GPIO_PIN_RESET);
                }
            }
            else
            {
                // Không chạy PID → chạy speed mặc định
                __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 500);
                HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, GPIO_PIN_SET);
                HAL_GPIO_WritePin(IN1_GPIO_Port, IN2_Pin, GPIO_PIN_RESET);
            }
        }

        // ======================
        // STOP: Slow down smoothly
        // ======================
        else if (conveyor_start == 0)
        {
            if (g_target_speed > 0)
            {
                g_target_speed -= 2;     // giảm từ từ mỗi chu kỳ TIM4 (10ms gốc)
            }
            else
            {
                g_target_speed = 0;
                pid_inst.output = 0;

                // Motor OFF hoàn toàn
                __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 0);
                HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, GPIO_PIN_RESET);
                HAL_GPIO_WritePin(IN1_GPIO_Port, IN2_Pin, GPIO_PIN_RESET);
            }

            // Áp PID ở tốc độ giảm dần
            error = (int16_t)g_target_speed - motor_vel;
            apply_pid(&pid_inst, error);

            if (pid_inst.output > 0)
            {
                __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, pid_inst.output);
                HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, GPIO_PIN_SET);
                HAL_GPIO_WritePin(IN1_GPIO_Port, IN2_Pin, GPIO_PIN_RESET);
            }
            else
            {
                __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, -pid_inst.output);
                HAL_GPIO_WritePin(IN1_GPIO_Port, IN1_Pin, GPIO_PIN_RESET);
                HAL_GPIO_WritePin(IN1_GPIO_Port, IN2_Pin, GPIO_PIN_RESET);
            }
        }

        send_counter++;
        if (send_counter >= 10)   // 10 chu kỳ * 10ms = 100ms
        {
            send_counter = 0;

            char msg[50];
            snprintf(msg, sizeof(msg), "RPM:%d\n", (int)motor_vel);

            CDC_Transmit_FS((uint8_t*)msg, strlen(msg));
        }
    }

    // TIM3: encoder direction detect
    if (htim->Instance == TIM3)
    {
        if (__HAL_TIM_IS_TIM_COUNTING_DOWN(&htim3))
            motor_enc.total_pulses -= ENCODER_RESOLUTION;
        else
            motor_enc.total_pulses += ENCODER_RESOLUTION;
    }
}

